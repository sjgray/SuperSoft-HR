*=$1000

L_1000:	       ;SEI
               LDA imagelow ;USR Function Jump Instr (4C)
               STA $03F0
               LDA imagehi ;USR Address Low Byte / High Byte
               STA $03F1

               LDA $03F2
               ASL
               TAX

               LDA DIMAGE,X
               STA imagelow ;USR Function Jump Instr (4C)
               INX
               LDA DIMAGE,X
               STA imagehi ;USR Address Low Byte / High Byte

               LDX #$0A
               STX $03F4
		LDX #$00
	
L_1022:       LDA #$00	; set low byte for screen ram to 0
			;	       STA screencount  
			; offset that will be added to base 1K address to set high byte for that ram block
 	       TAY		
L_1023:		PHA
L_1024:        LDA #$02	; set low byte for screen ram to 0
               STA screenlow 	; set low byte for screen ram             

	       LDA SCREENP,Y	; get supersoft memory high byte address
               STA screenhigh 	; set high screen byte		
L_1028:        LDY #$00
               LDA (imagelow),Y ;USR Function Jump Instr (4C)
	       STX xstore
	       LDY xstore
               STA (screenlow),Y

               INC imagelow ;USR Function Jump Instr (4C)
               BNE L_1035
               INC imagehi ;USR Address Low Byte / High Byte

L_1035:        INX
               CPX $03F4   ; have we draw 11 pixels
               BNE L_1028

               TXA
               SEC
               SBC #$0A
               TAX              
		PLA
               TAY
               INY
               TYA
               CPY #$8
               BNE L_1023
               LDA $03F4
               CLC
               ADC #$28
               STA $03F4
               TXA
               CLC
               ADC #$28
               TAX
               CPX #$F0
               BNE L_1022
               LDA $03F0
               STA imagelow ;USR Function Jump Instr (4C)
               LDA $03F1
               STA imagehi ;USR Address Low Byte / High Byte
               ;CLI
               RTS

